class Planner:
    """Generates execution plans using an LLM."""

    def __init__(self, llm: Any):
        self.llm = llm

    def create_plan(self, user_message: str, history_messages: Optional[List[Any]] = None) -> Plan:
        """Generate a Plan from the user message."""
        import re as _re
        today = date.today().strftime("%Y-%m-%d")
        system_content = build_plan_prompt(today)

        messages = [SystemMessage(content=system_content)]
        if history_messages:
            for msg in history_messages[-6:]:  # 从4条扩展到6条
                content = msg.content if hasattr(msg, "content") else str(msg)
                if len(content) > 800:
                    # 先提取 ID，截断后补回，确保上下文引用不丢失
                    ids_found = _re.findall(r'\[ID:\d+\]', content)
                    content = content[:700]
                    if ids_found:
                        content += "... [涉及活动: " + " ".join(ids_found) + "]"
                    else:
                        content += "..."
                if isinstance(msg, HumanMessage):
                    messages.append(HumanMessage(content=content))
                else:
                    from langchain_core.messages import AIMessage
                    if isinstance(msg, AIMessage):
                        messages.append(AIMessage(content=content))
        messages.append(HumanMessage(content=user_message))

        try:
            response = self.llm.invoke(messages)
            raw = response.content

            if isinstance(raw, list):
                texts = [item.get("text", "") for item in raw if isinstance(item, dict) and item.get("type") == "text"]
                content = "".join(texts)
            else:
                content = str(raw)

            # Extract JSON
            import re as _re
            json_match = _re.search(r'```(?:json)?\s*(\{.*?\})\s*```', content, _re.DOTALL)
            if json_match:
                content = json_match.group(1)

            plan_dict = json.loads(content.strip())
            logger.info(f"Plan: {plan_dict}")

            return Plan(
                mode=plan_dict.get("mode", "tool"),
                tool_name=plan_dict.get("tool_name"),
                params=plan_dict.get("params"),
                code=plan_dict.get("code"),
                reasoning=plan_dict.get("reasoning", ""),
                reply=plan_dict.get("reply"),
            )
        except Exception as e:
            logger.warning(f"Plan generation failed: {e}")
            # Fallback to get_latest_activity
            return Plan(
                mode="tool",
                tool_name="get_latest_activity",
                params={},
                reasoning=f"计划生成失败，fallback: {e}",
            )
