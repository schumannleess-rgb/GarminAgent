# ==========================================
# Sandbox Executor
# ==========================================

class SandboxExecutor:
    """Restricted Python code execution environment for the agent.

    - Pre-injects `client` (GarminClient) and safe standard libraries
    - Restricts builtins to prevent dangerous operations
    - Captures stdout and the `result` variable
    - Enforces execution timeout
    """

    def __init__(self, client: Any, formatters_module: Any = None):
        self.client = client
        self.formatters = formatters_module

    def _get_safe_globals(self) -> Dict[str, Any]:
        """Build the restricted globals dict for code execution."""
        import collections
        import statistics
        import math
        import re
        import datetime

        safe_builtins = {
            "True": True,
            "False": False,
            "None": None,
            "len": len,
            "range": range,
            "enumerate": enumerate,
            "zip": zip,
            "map": map,
            "filter": filter,
            "sum": sum,
            "min": min,
            "max": max,
            "abs": abs,
            "round": round,
            "sorted": sorted,
            "list": list,
            "dict": dict,
            "tuple": tuple,
            "set": set,
            "str": str,
            "int": int,
            "float": float,
            "bool": bool,
            "print": print,
            "isinstance": isinstance,
            "hasattr": hasattr,
            "getattr": getattr,
            "setattr": setattr,
            "KeyError": KeyError,
            "ValueError": ValueError,
            "TypeError": TypeError,
            "AttributeError": AttributeError,
            "IndexError": IndexError,
            "Exception": Exception,
            "RuntimeError": RuntimeError,
        }

        globals_dict: Dict[str, Any] = {
            "__builtins__": safe_builtins,
            "client": self.client,
            "json": json,
            "datetime": datetime,
            "re": re,
            "math": math,
            "collections": collections,
            "statistics": statistics,
        }

        # Inject formatter helpers if available
        if self.formatters is not None:
            fmt = self.formatters
            for fname in [
                "format_distance", "format_duration", "format_pace",
                "format_pace_from_min_km", "format_heart_rate",
                "format_cadence", "format_date", "format_gct",
                "format_vo", "format_stride", "format_calories",
                "format_duration_minutes", "format_running_dynamics",
                "format_split_pace",
            ]:
                if hasattr(fmt, fname):
                    globals_dict[fname] = getattr(fmt, fname)

        return globals_dict

    def _run_code(self, code: str) -> Dict[str, Any]:
        """Actually run the code (runs in a separate thread for timeout control)."""
        stdout_capture = io.StringIO()
        locals_dict: Dict[str, Any] = {}

        with contextlib.redirect_stdout(stdout_capture):
            compiled = compile(code, "<agent_sandbox>", "exec")
            exec(compiled, self._get_safe_globals(), locals_dict)

        return {
            "stdout": stdout_capture.getvalue(),
            "result": locals_dict.get("result"),
            "locals": {k: v for k, v in locals_dict.items() if not k.startswith("_")},
        }

    def execute(self, code: str, timeout: int = 30) -> Dict[str, Any]:
        """Execute code with timeout and error isolation.

        Returns:
            Dict with keys: stdout, result, error, locals
        """
        with ThreadPoolExecutor(max_workers=1) as executor:
            future = executor.submit(self._run_code, code)
            try:
                result = future.result(timeout=timeout)
                result["error"] = None
                return result
            except FutureTimeoutError:
                return {
                    "stdout": "",
                    "result": None,
                    "error": f"代码执行超时（>{timeout}秒）。可能是循环未终止或 API 调用过多。",
                    "locals": {},
                }
            except Exception as e:
                return {
                    "stdout": "",
                    "result": None,
                    "error": f"{type(e).__name__}: {e}\n{traceback.format_exc()}",
                    "locals": {},
                }


